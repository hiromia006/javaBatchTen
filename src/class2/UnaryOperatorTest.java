package class2;

public class UnaryOperatorTest {
    public static void main(String[] args) {
        int number = 70;
        System.out.println(number--);//70 after print: 69
        System.out.println(number++); // 69 after print: 70
        System.out.println(number); //70
        System.out.println(++number); // 71, After print: 71
        System.out.println(--number); // 70, After print: 70
        System.out.println(number); // 70


    }
}
