package class2;

public class RelationalOperatorTest {
    public static void main(String[] args) {
        int number1 = 99, number2 = 20;
        System.out.println(number1 < number2); // 199 < 20
        System.out.println(number1 <= number2); // 199 <=20
        System.out.println(number1 > number2); // 199 >
        System.out.println(number1 >= number2);
        System.out.println(number1 == number2);
        System.out.println(number1 != number2);



    }
}
